package outils;

public class StructureUV {

	private double _u, _v;

	public StructureUV(double u, double v) {

		this._u = u;
		this._v = v;
	}

	public double get_u() {
		return _u;
	}

	public void set_u(double _u) {
		this._u = _u;
	}

	public double get_v() {
		return _v;
	}

	public void set_v(double _v) {
		this._v = _v;
	}

}
